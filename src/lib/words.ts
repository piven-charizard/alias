export const ALL_WORDS: Word[] = [...EASY_WORDS, ...MEDIUM_WORDS, ...HARD_WORDS];

export function getFilteredWords(difficulty: "easy" | "medium" | "hard" | "mixed"): Word[] {
  if (difficulty === "mixed") return ALL_WORDS;
  return ALL_WORDS.filter((w) => w.difficulty === difficulty);
}

export function shuffleWords(words: Word[]): Word[] {
  const shuffled = [...words];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}
